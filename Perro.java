public class Perro {
    private String nombre;
    private int edad;
    private String raza;

    public String getNombre() {
        return nombre;
    }
    public void setNombre(String n) {
        this.nombre = n;
    }

    public int getEdad() {
        return edad;
    }
    public void setEdad(int e) {
        this.edad = e;
    }

    public String getRaza() {
        return raza;
    }
    public void setRaza(String r) {
        this.raza = r;
    }    
}